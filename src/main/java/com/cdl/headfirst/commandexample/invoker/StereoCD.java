package com.cdl.headfirst.commandexample.invoker;

/**
 * 立体CD
 */
public class StereoCD {

    public void on() {
        System.out.println("打开立体CD");
    }

    public void setCD() {
        System.out.println("放入CD");
    }

    public void setVolume() {
        System.out.println("调节音量");
    }

    public void off() {
        System.out.println("关闭立体CD");
    }

}
